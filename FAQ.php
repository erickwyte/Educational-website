<script>
    // FAQ accordion functionality
    document.querySelectorAll('.faq-question').forEach(question => {
        question.addEventListener('click', () => {
            const item = question.parentElement;
            item.classList.toggle('active');
        });
    });

    // Search functionality
    const searchInput = document.querySelector('.faq-search input');
    searchInput.addEventListener('keyup', () => {
        const searchTerm = searchInput.value.toLowerCase();
        document.querySelectorAll('.faq-item').forEach(item => {
            const question = item.querySelector('.faq-question').textContent.toLowerCase();
            const answer = item.querySelector('.faq-answer').textContent.toLowerCase();
            
            if (question.includes(searchTerm) || answer.includes(searchTerm)) {
                item.style.display = 'block';
            } else {
                item.style.display = 'none';
            }
        });
    });

    // Category filtering
    document.querySelectorAll('.faq-category').forEach(category => {
        category.addEventListener('click', () => {
            document.querySelectorAll('.faq-category').forEach(cat => cat.classList.remove('active'));
            category.classList.add('active');
            
            // Simple demonstration of category filtering
            // In a real implementation, you would have proper categories assigned to questions
            const categoryText = category.textContent.toLowerCase();
            
            document.querySelectorAll('.faq-item').forEach(item => {
                if (categoryText === 'all questions') {
                    item.style.display = 'block';
                } else {
                    // This is a simplified example - you would need to add data attributes for proper filtering
                    item.style.display = 'none';
                }
            });
        });
    });
